<h1> About </h1>





<BODY LANG="en-US" LINK="#0563c1" DIR="LTR">
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
Vāda Project is a platform for collaborative and reason- and
evidence-sensitive debate that is built on a contemporary
systematization of particular Indian theories of knowledge sources
(</FONT><I>pramāṇa</I></FONT>)</FONT><I>
</I></FONT>and methods of
reasoning (</FONT><I>nyāya</I></FONT>).
This systematization is most directly indebted to the theories and
methods both of the Nyāya philosophy of 
<a href = "https://en.wikipedia.org/wiki/Ny%C4%81ya_S%C5%ABtras" > Akṣapāda Gautama and his successors</a>, and of the Buddhist philosophy of
<a href = "https://plato.stanford.edu/entries/dharmakiirti/" > Dharmakīrti</a> and his
successors.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
purpose of a </FONT><I>vāda</I></FONT>
debate is to arrive at knowledge (</FONT><I>pramā</I></FONT>)
by resolving doubt through accredited </FONT><I>pramāṇa</I></FONT>s,
in a way that is supplemented by philosophical argument (</FONT><I>tarka</I></FONT>).
According to sūtra 1.1.23 of Gautama’s </FONT><I>Nyāya-Sutra</I></FONT>,
“doubt (</FONT><I>saṃśaya</I></FONT>)
is deliberative awareness in need of details about something
particular” (Dasti and Phillips: 41).  In other words, when doubt
happens, there is awareness of a particular thing, but uncertainty
about one or more properties located in it, or, to use another
phrasing, there is uncertainty about the subject’s possession of
one or more predicates.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">According
to Uddyotakara’s commentary on </FONT><I>Nyāya-Sūtra
</I></FONT>1.1.23, such
uncertainty can happen within oneself or among two or more persons.
Regardless, when there is doubt about a particular subject (p), such
as a tall, thin thing perceived in the distance, both a thesis and at
least one counter-thesis emerge. One might first think, “The tall,
thin thing in the distance is a post” (Tp), and then think, “The
tall, thin thing in the distance is a person” (Cp). Due to this
uncertainty, one does not know how to proceed, and one’s actions
are disrupted. The purpose of </FONT><I>vāda</I></FONT>
is to resolve such doubts in ways that empower persons to attain ends
they desire.  </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">As
a platform for collaborative public deliberation, the Vāda Project
especially seeks to help persons to resolve doubts produced from
controversy (</FONT><I>vipratipratti</I></FONT>),
which arises when persons voice contradictory judgments about
particular subjects. As Uddyotakara writes in his commentary on
</FONT><I>Nyāya-Sūtra</I></FONT>
1.1.23, “The cause of doubt that arises from controversy depends on
there being speakers or advocates. ‘Which side is proposing the
correct view and which is proposing the incorrect view?’ Such doubt
arises for someone listening to the dispute” (Dasti and Phillips:
42). During the development of Indian theories and methods of
</FONT><I>pramāṇa-vāda</I></FONT>,
controversies about the existence or non-existence of enduring
selves, the reliability and validity of the Vedas and Vedic
injunctions, and the nature of causality, among other subjects,
abounded. These controversies generated significant doubt, and as a
result, persons’ actions were disrupted. “Should I adhere to
Vedic prescriptions, or should I leave my household and meditate on
the five aggregates?” The theoretical purpose of </FONT><I>vāda</I></FONT>,
then, was to resolve such doubts in ways that would enable persons to
achieve their desired ends. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">In
contemporary times, too, actions can be stymied by doubts produced
from public controversy. Through news networks, social media
platforms, religious communities, university curricula and
specialized publications, speakers and advocates articulate
contradictory views about the same subjects. As a result, onlookers
may not know which views are correct and which are incorrect, and
action can be disrupted. The purpose of the Vāda Project platform is
to resolve such doubts in ways that empower the public and its
diverse members to attain desired ends.</FONT></P>
