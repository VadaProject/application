<h1> About </h1>
The Vāda Project is a platform for collaborative and reason- and evidence-sensitive debate that is built on a contemporary systematization of particular Indian theories of knowledge sources (pramāṇa) and methods of reasoning (nyāya). This systematization is most directly indebted to the theories and methods both of the Nyāya philosophy of Akṣapāda Gautama and his successors, and of the Buddhist philosophy of Dharmakīrti and his successors. 
<br><br>
The purpose of a vāda debate is to arrive at knowledge (pramā) by resolving doubt through accredited pramāṇas, in a way that is supplemented by philosophical argument (tarka). According to Gautama’s Nyāya-Sutra, “doubt (saṃśaya) is deliberative awareness in need of details about something particular” (1.1.23).  In other words, when doubt happens, there is awareness of a particular thing, but uncertainty about one or more properties located in it, or, to use a more common contemporary phrasing, there is uncertainty about the subject’s possession of one or more predicates.
<br><br>
According to Uddyotakara’s commentary on Nyāya-Sūtra 1.1.23, such uncertainty can happen within oneself or among two or more persons. Regardless, when there is doubt about a particular subject (p), such as a tall, thin thing perceived in the distance, both a thesis and at least one counter-thesis emerge. One might first think, “The tall, thin thing in the distance is a post” (Tp), and then think, “The tall, thin thing in the distance is a person” (Cp). Due to this uncertainty, one does not know how to proceed, and one’s actions are disrupted. The purpose of vāda is to resolve such doubts in ways that empower persons to attain ends they desire.  
<br><br>
As a platform for collaborative public deliberation, the Vāda Project especially seeks to help persons to resolve doubts produced from controversy (vipratipratti), which arises when persons voice contradictory judgments about particular subjects. As Uddyotakara writes in his commentary on Nyāya-Sūtra 1.1.23, “The cause of doubt that arises from controversy depends on there being speakers or advocates. ‘Which side is proposing the correct view and which is proposing the incorrect view?’ Such doubt arises for someone listening to the dispute.” During the development of Indian theories and methods of pramāṇa-vāda, controversies about the existence or non-existence of enduring selves, the reliability of the Vedas  and the validity of Vedic injunctions, and the nature of causality, among other  subjects, abounded. These controversies generated significant doubt, and as a result, persons’ actions were disrupted. “Should I adhere to Vedic prescriptions, or should I leave my household and meditate on the five aggregates?” The theoretical purpose of vāda, then, was to resolve such doubts in ways that would enable persons to achieve their desired ends. 
<br><br>
In contemporary times, too, actions are also stymied by doubts produced from public controversy. Through news networks, social media platforms and specialized publications, speakers and advocates articulate contradictory views about the same subjects. As a result, onlookers do not know which views are correct and which are incorrect, and public action is disrupted. The purpose of the Vāda Project platform is to resolve such doubts in ways that empower the public and its members to attain their desired ends.
