A claim is the most basic element of debate on the V&#257;da Project platform. Each
claim consists of a <b>subject</b> (<i>pak&#7779;a</i>) and a <b>target
property</b> (<i>s&#257;dhya</i>). A subject is what a claim is about. A target
property is what a claim asserts about a subject. For example, in the claim
“The hill is on fire,” the subject is “the hill” and the target property is “is
on fire.” <o:p></o:p></span></p>

When
entering a claim, a user will be prompted to enter a subject and a target
property. To minimize grammatical bugs in later statements, the user will be
prompted to indicate whether the subject is a person or a thing.<o:p></o:p></span></p>

All
claims entered on the V&#257;da Project platform must be justified through one
of four <b>support means</b>. Support means are distinct ways of supporting
claims. In this iteration of the V&#257;da Project platform, support means
include three <b>knowledge sources</b> (<span class=SpellE><i>pram&#257;&#7751;a</i>s</span>)
and <b>philosophical argument</b> (<i>tarka</i>). <o:p></o:p></span></p>

On
the V&#257;da Project platform, philosophical argument is a method of
administratively moderated argument that is conducted through a Facebook
plugin; as <span class=SpellE>V&#257;tsy&#257;yana</span> writes in his
commentary on <i>Ny&#257;ya S&#363;tra </i>1.1.40, in a <i>v&#257;da</i>
debate, <i>tarka</i> serves only a supplementary role to debate that is
conducted through appeal to knowledge sources (<span class=SpellE>Dasti</span>
and Phillips: 46). The three knowledge sources in this iteration of the
V&#257;da Project platform are <b>inference</b>, <b>testimony</b>, and <b>perception</b>.<o:p></o:p></span></p>

<o:p>&nbsp;</o:p></span></p>


<h2 id="Inference"> Inference </h2><o:p></o:p></span></i></p>

Inference
(<span class=SpellE><i>anum&#257;na</i></span>)<i> </i>is a way of generating
knowledge about something that is presently uncertain through knowledge about something
that is presently certain. In a <i>v&#257;da</i> debate, all initial claims should
be controversial, or uncertain. To support a claim through inference, a user
asserts that the claim’s subject is known to possess a <b>reason</b> <b>property
</b>(<i>hetu</i>)<i> </i>that is invariably present with the claim’s target
property.<span style='mso-spacerun:yes'>   </span><o:p></o:p></span></p>

For
example, a user might select “inference” as a support means to justify the
claim, “The hill is on fire.” Afterwards, when prompted to enter a reason
property, the user might type “has smoke billowing from it” in the “reason
property” field.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

To
help to establish that the reason property is known to be invariably present
with the target property, the user will be prompted to enter an <b>example</b>
of a similar subject (<span class=SpellE><i>sapak&#7779;a</i></span>). An appropriate
similar subject will differ from the subject of the claim (the hill) and be
known to possess both the reason property and the target property of the
inference. For example, the user might enter “a kitchen” in the “example” field
and click “submit.”<o:p></o:p></span></p>


After
clicking “submit,” the user will be notified that the claim has been submitted.
Upon returning to the debate topic page, users will see that the claim has been
added. By clicking on the “details” link on the claim box, users will see that
the inference has automatically generated the following three statements: <o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Thesis: The hill is on fire.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Reason: The hill has smoke
billowing from it.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Rule &amp; Example: Whatever has
smoke billowing from it is on fire, as in the case of a kitchen.<o:p></o:p></span></p>

The
thesis statement of the inference conjoins the subject with the target property
of the claim. The reason statement conjoins the same subject with the reason
property. The rule and example statement <span class=GramE>is</span> an
automatically generated assertion that the target property is present in whatever
the reason property is present in, and that a similar subject exemplifies this universal
association.<o:p></o:p></span></p>

If
they would like, users can now raise doubts about the claim by clicking the “<b>Flag
this claim</b>” tab from the “details” pop-up. Flags that can be raised against
a claim are specific to the knowledge source that supports it. <b><o:p></o:p></b></span></p>

The
following flags can be raised against the thesis, reason and rule &amp; example
statements of a claim supported by inference:<o:p></o:p></span></p>

