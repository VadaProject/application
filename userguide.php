<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in"><h2 id="Testimony"> Testimony </h2></span></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Testimony
(</FONT><I>śabda</I></FONT>)
is a way of generating knowledge about something previously uncertain
through the words of a </FONT><B>trustworthy
authority</B></FONT> (</FONT><I>āpta</I></FONT>).
As Vātsyāyana writes in his commentary on </FONT><I>Nyāya
Sūtra</I></FONT> 1.1.7, “A
trustworthy authority is someone who knows something directly, an
instructor with the desire to communicate it faithfully as it is
known” (Dasti and Phillips: 35).</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
Sanskrit term </FONT><I>śabda</I></FONT>
means “word.” When one comes to know something through testimony,
words are the cause or instrument of this knowledge.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Yogācāra
Buddhist and other philosophers contested </FONT><I>śabda</I></FONT>’s
status as a source of knowledge, and preferred to limit </FONT><I>pramāṇa</I></FONT>s
in </FONT><I>vāda</I></FONT>
debates to sources such as inference and perception. However, Nyāya
philosophers argued that to refuse to acknowledge testimony as a
</FONT><I>pramāṇa</I></FONT>
would contradict lived epistemic practice. In their everyday lives,
even Buddhists who claim to reject </FONT><I>śabda</I></FONT>
recognize the Buddha’s words and other textual and verbal sources
as trustworthy authorities, they argued. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Buddhist
philosophers such as Dignāga and Dharmakīrti responded by
attempting to show that in day-to-day life, knowing through words
does not practically differ from knowing through inference.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">For
the sake of coordinating open, truth-directed debate about
contemporary controversies among users of diverse backgrounds and
perspectives, this iteration of the Vāda Project platform includes
testimony as a source of knowledge. However, it might theoretically
be possible to translate all verbal and textual claims made by
supposedly trustworthy authorities into the standard logical form of
an inference. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Users
are expected to select inference as a support means for claims that
various speakers and advocates attempt to justify, not by relying on
the direct knowledge of particular, supposedly trustworthy
authorities, but on the basis of recurring reasons and examples. For
example, critics of abortion commonly put forward the following
inference:</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Thesis:
Abortion should be illegal.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Reason:
Abortion is murder.   </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Rule
&amp; Example: Whatever is murder should be illegal, as in the case
of infanticide.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">When
speakers and advocates use reasons, general rules, and examples to
support claims without appealing to the direct knowledge of
particular textual or verbal sources, inference is the appropriate
support means.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Even
if an argument appeals to the supposedly direct knowledge of a
particular textual or verbal source, users can sometimes translate
this argument into the standard logical form of an inference without
much distortion. For example, specific scientific testimony in the
area of fetal neurology<A CLASS="sdendnoteanc" NAME="sdendnote2anc" HREF="#sdendnote2sym"><SUP>ii</SUP></A>
might be translated into the following inference: </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Thesis:
A human fetus in the first trimester of pregnancy lacks capacity for
sentience.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Reason:
A human fetus in the first trimester of pregnancy lacks established
and functional somatosensory pathways from the periphery to the
primary somatosensory region of the cerebral cortex. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Rule
and Example: Whatever lacks established and functional somatosensory
pathways from the periphery to the primary somatosensory region of
the cerebral cortex lacks capacity for sentience, as in the case of a
human zygote.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">However,
to exclude </FONT><I>śabda</I></FONT>
as a </FONT><I>pramāṇa</I></FONT>
and insist that users be equipped to do such translation work would
be both cumbersome and counterproductive to the purposes of this
iteration of the Vāda Project platform. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">First,
extracting reason properties and examples from written and verbal
sources that are composed in various rhetorical styles is typically
an art and not a mechanical process. Because not all textual or
verbal sources state specific reason properties or similar cases,
translators usually need to create their own examples and reason
properties either wholesale or from scattered textual elements. This
sort of translation work can be time consuming and difficult; it is
also unnecessary if testimony is made available, contingently, as a
plausible support means for the Vāda Project platform. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Second,
to insist that all textual and verbal testimony be translated into
the standard logical form of an inference would become increasingly
counterproductive as textual and verbal sources become more and more
specialized. To be able to support technical claims whether through
testimony or inference, users must have or be equipped to develop a
degree of understanding in subject areas as varied, to use the topic
of the inferences above as a case in point, as fetal neurology,
privacy law, and criminal homicide codes. But to require that all
users master these various technical subject areas to an extent that
would enable them to translate specialized arguments into the
standard logical form of an inference would limit the abilities of
diverse users to debate controversial topics on the platform. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">This
first iteration of the Vāda Project platform therefore contingently
includes </FONT><I>śabda</I></FONT>
as a support means—to preclude testimony would impose unnecessary
burdens and be counterproductive to the Vāda Project’s aim of
coordinating open, truth-directed debate about contemporary
controversies among users of diverse backgrounds and perspectives. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">As
stated above, testimony is a way of acquiring knowledge through the
words of a </FONT><B>trustworthy
authority</B></FONT> (</FONT><I>āpta</I></FONT>).
Because well-intentioned actions can err, and because persons who
faithfully desire to communicate what they know can misspeak or
miswrite what they intend to convey, this iteration of the Vāda
Project platform adopts the following definition of an </FONT><I>āpta</I></FONT>:
</FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">A
trustworthy authority is a textual or verbal source that knows
something directly, desires to communicate it faithfully as it is
known, and fulfills this aim.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">For
Nyāya philosophers, a textual or verbal source’s status as an </FONT><I>āpta</I></FONT>
depends on reality rather than on the subjective assessments of
readers or hearers. It either is or is not the case that a source has
direct knowledge about what is testified, faithfully intends to
communicate what is known, and manages to do so, no matter what
readers and hearers personally think. If, as a matter of fact, any
condition is not met, then regardless of the authority’s
credentials and social status, the authority is not trustworthy.
Conversely, if all conditions are met, then the authority </FONT><I>is</I></FONT>
trustworthy, regardless of the authority’s possible lack of
credentials and social status or generally dodgy demeanor. As
Vātsyāyana writes in his commentary on </FONT><I>Nyāya
Sūtra </I></FONT>1.1.7, “To be
trustworthy is to have direct knowledge of something. One who
operates on this basis is a </FONT><B>trustworthy
authority</B></FONT>. This
criterion applies equally to sages, respected members of one’s own
community, and those outside the fold” (Dasti and Phillips: 35).   
</FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">To
support a claim through testimony, users should select “testimony”
as a support means for the claim. They will then be prompted to enter
a URL that links to the textual or verbal source in a text box
labeled “Enter Speech/Research Document,” and to transcribe, or
copy and paste, relevant words from the source in a text box labeled
“Relevant Excerpt. Include timestamps for audio/video, if
applicable.” The transcription should be verbatim.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">After
clicking “submit,” the user will be notified that the claim has
been submitted. Upon returning to the debate topic page, users will
