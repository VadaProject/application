<h2 id="Testimony"> Testimony </h2></span></i><span
style='font-family:"Times New Roman",serif'><o:p></o:p></span></p>

Testimony
(<i>&#347;abda</i>) is a way of generating knowledge about something uncertain
through the words of a <b>trustworthy authority</b> (<span class=SpellE><i>&#257;pta</i></span>).
As <span class=SpellE>V&#257;tsy&#257;yana</span> writes in his commentary on <i>Ny&#257;ya
S&#363;tra</i> 1.1.7, “A trustworthy authority is someone who knows something
directly, an instructor with the desire to communicate it faithfully as it is
known” (<span class=SpellE>Dasti</span> and Phillips: 35).<o:p></o:p></span></p>

The
Sanskrit term <i>&#347;abda</i> means “word.” When one comes to know something
through testimony, words are the cause or instrument of this knowledge.<o:p></o:p></span></p>

<span class=SpellE><span
style='font-family:"Times New Roman",serif'>Yog&#257;c&#257;ra</span></span><span
style='font-family:"Times New Roman",serif'> Buddhist and other philosophers
contested <span class=SpellE><i>&#347;abda</i>’s</span> status as a source of
knowledge, and preferred to limit <span class=SpellE><i>pram&#257;&#7751;a</i>s</span>
in <span class=SpellE><i>v&#257;da</i></span> debates to sources such as
inference and perception. However, Ny&#257;ya philosophers argued that to
refuse to acknowledge testimony as a <i>pram&#257;&#7751;a</i> would contradict
lived epistemic practice. In their everyday lives, even Buddhists who claim to reject
<i>&#347;abda</i> recognize the Buddha’s words and other textual and verbal sources
as trustworthy authorities, they argued. <o:p></o:p></span></p>

Buddhist
philosophers such as <span class=SpellE>Dign&#257;ga</span> and <span
class=SpellE>Dharmak&#299;rti</span> responded by attempting to show that in
day-to-day life, knowing through words does not practically differ from knowing
through inference.<o:p></o:p></span></p>

For
the sake of simplicity, this iteration of the V&#257;da Project platform
includes testimony as a source of knowledge. While it might theoretically be
possible to translate all verbal claims made by supposedly trustworthy
authorities into the standard logical form of an inference, it would arguably
be cumbersome and counterproductive to demand this, especially for one contingent
iteration of the V&#257;da Project platform. Excluding <i>&#347;abda</i> would
be cumbersome because extracting reason properties and examples from written
and verbal sources that are composed in various rhetorical styles is difficult and
more of an art than a mechanical process. Because not all textual or verbal
sources state specific reason properties or similar cases, translators usually need
to create their own examples and reason properties either wholesale or from scattered
textual elements. <span style='mso-spacerun:yes'> </span><span
style='mso-spacerun:yes'> </span><o:p></o:p></span></p>

Also,
excluding testimony and relying only on inference and perception would be counterproductive.
While including testimony as a source of knowledge does not preclude users from
translating arguments from textual and verbal sources into the standard logical
form of an inference, excluding testimony as a support means limits the
abilities of diverse users to debate controversial topics on the platform. <o:p></o:p></span></p>

First,
users are expected to select inference as a support means for claims that
various speakers and advocates attempt to justify, not by relying on the direct
knowledge of particular, supposedly trustworthy authorities, but on the basis
of recurring reasons and examples. For example, critics of abortion commonly
put forward the following inference:<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Thesis: Abortion should be illegal.<o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Reason: Abortion is murder.<span
style='mso-spacerun:yes'>  </span><span style='mso-spacerun:yes'> </span><o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>Rule &amp; Example: Whatever is
murder should be illegal, as in the case of infanticide.<o:p></o:p></span></p>

When
speakers and advocates use reasons, general rules, and examples to support
claims without appealing to the direct knowledge of particular textual or
verbal sources, inference is the appropriate support means. Second, even if an
argument appeals to the supposedly direct knowledge of a particular textual or
verbal source, users can sometimes translate this argument into the standard
logical form of an inference without too much distortion. However, to insist
that all textual and verbal testimony be translated either into the standard
logical form of an inference or perception would become counterproductive as
textual and verbal sources become more specialized and technical. To be able to
translate technical arguments into the standard logical form of an inference,
users would need to have or be equipped to develop increasingly specialized
skills in subject areas as varied, depending on the topic of debate, as fetal
neurology, privacy law, and criminal homicide codes. To insist that all users
of the V&#257;da Project platform either have or be equipped to have such
far-ranging expertise, as well as the creative ability to translate this
far-ranging expertise into the standard logical form of an inference, rather
than simply, but contingently, accepting <i>&#347;abda</i> as a <i>pram&#257;&#7751;a</i>
would be counterproductive to the V&#257;da Project’s aim of coordinating open,
truth-directed debate about contemporary controversies among users of diverse
backgrounds and perspectives. <o:p></o:p></span></p>

As
stated above, testimony is a way of acquiring knowledge through the words of a <b>trustworthy
authority</b> (<span class=SpellE><i>&#257;pta</i></span>). Because
well-intentioned actions can err, and because persons who faithfully desire to
communicate what they know can misspeak or miswrite what they intend to convey,
this iteration of the V&#257;da Project platform adopts the following
definition of an <span class=SpellE><i>&#257;pta</i></span>: <o:p></o:p></span></p>

<p class=MsoNormal style='margin-left:.5in;text-align:justify'><span
style='font-family:"Times New Roman",serif'>A trustworthy authority is a
textual or verbal source who knows something directly, desires to communicate
it faithfully as it is known, and fulfills this desire.<o:p></o:p></span></p>

For
Ny&#257;ya philosophers, a textual or verbal source’s status as an <span
class=SpellE><i>&#257;pta</i></span> depends on reality rather than the
subjective assessments of readers or hearers. It either is or is not the case
that a source has direct knowledge about what is testified, faithfully intends
to communicate what is known, and manages to do so, no matter what readers and
hearers personally think. If, as a matter of fact, any condition is not met,
then regardless of the authority’s credentials and social status, the authority
is not trustworthy. Conversely, if all conditions are met, then the authority <i>is</i>
trustworthy, regardless of the authority’s possible lack of credentials and
social status. As <span class=SpellE>V&#257;tsy&#257;yana</span> writes in his
commentary on <i>Ny&#257;ya S&#363;tra </i>1.1.7, “To be trustworthy is to have
direct knowledge of something. One who operates on this basis is a <b>trustworthy
authority</b>. This criterion applies equally to sages, respected members of
one’s own community, and those outside the fold” (<span class=SpellE>Dasti</span>
and Phillips: 35). <span style='mso-spacerun:yes'>  </span><span
style='mso-spacerun:yes'> </span><o:p></o:p></span></p>

To
support a claim through testimony, users should select “testimony” as a support
means for the claim. They will then be prompted to enter a URL that links to
the textual or verbal source in a text box labeled “Enter Speech/Research
Document,” and to transcribe, or copy and paste, relevant words from the source
in a text box labeled “Summary of Argument/Excerpt. Include timestamps for
video, if applicable.” The transcription should be verbatim.<o:p></o:p></span></p>

After
clicking “submit,” the user will be notified that the claim has been submitted.
Upon returning to the debate topic page, users will see that the claim has been
added. By clicking on the “details” link on the claim box, users will see that
the inference has generated a hyperlink to the textual or verbal source and displays
<b>(!) </b>the transcribed words. <o:p></o:p></span></p>

If
they would like, users can now raise doubts about the claim by clicking the “<b>Flag</b>”
tab from the “details” pop-up. The following flags can be raised against a
