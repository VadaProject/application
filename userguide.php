link on the claim box, users will see that the inference has
automatically generated the following three statements: </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Thesis:
The hill is on fire.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Reason:
The hill has smoke billowing from it.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">Rule
