<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in"><h2 id="Inference"> Inference </h2><o:p></o:p></span></i></p>
</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Inference
(</FONT><I>anumāna</I></FONT>)</FONT><I>
</I></FONT>is a way of generating
knowledge about something that is presently uncertain through
knowledge about something that is presently certain. In a </FONT><I>vāda</I></FONT>
debate, all initial claims should be controversial, or uncertain. To
support a claim through inference, a user asserts that the claim’s
subject is known to possess a </FONT><B>reason</B></FONT>
</FONT><B>property </B></FONT>(</FONT><I>hetu</I></FONT>)</FONT><I>
</I></FONT>that is invariably
present with the claim’s target property.   </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">For
example, a user might select “inference” as a support means to
justify the claim, “The hill is on fire.” Afterwards, when
prompted to enter a reason property, the user might type “has smoke
billowing from it” in the “reason property” field.  </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">To
help to establish that the reason property is known to be invariably
present with the target property, the user will be prompted to enter
an </FONT><B>example</B></FONT>
of a similar subject (</FONT><I>sapakṣa</I></FONT>).
An appropriate similar subject will differ from the subject of the
claim (the hill) and be known to possess both the reason property and
the target property of the inference. For example, the user might
enter “a kitchen” in the “example” field and click “submit.”</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">After
clicking “submit,” the user will be notified that the claim has
been submitted. Upon returning to the debate topic page, users will
