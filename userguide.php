<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">A
claim is the most basic element of debate on the Vāda Project
platform. Each claim consists of a </FONT><B>subject</B></FONT>
(</FONT><I>pakṣa</I></FONT>)
and a </FONT><B>target property</B></FONT>
(</FONT><I>sādhya</I></FONT>).
A subject is what a claim is about. A target property is what a claim
asserts about a subject. For example, in the claim “The hill is on
fire,” the subject is “the hill” and the target property is “is
on fire.” </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">When
entering a claim, a user will be prompted to enter a subject and a
target property. To minimize grammatical bugs in later statements,
the user will be prompted to indicate whether the subject is a person
or a thing.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">All
claims entered on the Vāda Project platform must be justified
through one of four </FONT><B>support
means</B></FONT>. Support means
are distinct ways of supporting claims. In this iteration of the Vāda
Project platform, support means include three </FONT><B>knowledge
sources</B></FONT> (</FONT><I>pramāṇa</I></FONT>s)
and </FONT><B>philosophical
argument</B></FONT> (</FONT><I>tarka</I></FONT>).
</FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">On
the Vāda Project platform, philosophical argument is a method of
administratively moderated argument that is conducted through a
Facebook plugin; as Vātsyāyana writes in his commentary on </FONT><I>Nyāya
Sūtra </I></FONT>1.1.40, in a
</FONT><I>vāda</I></FONT>
debate, </FONT><I>tarka</I></FONT>
serves only a supplementary role to debate that is conducted through
appeal to knowledge sources.<A CLASS="sdendnoteanc" NAME="sdendnote1anc" HREF="#sdendnote1sym"><SUP>i</SUP></A>
The three knowledge sources in this iteration of the Vāda Project
platform are </FONT><B>inference</B></FONT>,
</FONT><B>testimony</B></FONT>,
