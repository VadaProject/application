<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in"><h2 id="Perception"> Perception </h2></span></FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Perception
(</FONT><I>pratyakṣa</I></FONT>)
is a way of generating knowledge about something previously uncertain
through sensory evidence. As Akṣapāda Gautama writes in </FONT><I>Nyāya
Sūtra</I></FONT> 1.1.4,
“Perceptual knowledge arises from connection of a sense faculty and
object, does not depend on language, is inerrant, and is definitive”
(Dasti and Phillips: 20).</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">For
the purpose of this digital platform, perceptual faculties are
limited to the audio and visual senses. Because smelling, tasting,
touching, and inner understanding (</FONT><I>manas</I></FONT>)
lack corresponding objects in digital space, there can be no
connection of these sense faculties with digital objects. Conversely,
contemporary A/V technologies allow hearing and seeing to connect
with audible and visible objects in the digital realm. To support
claims about smells, tastes, touches and inner awareness, users
should instead rely on testimony as a support means. To support
claims through audio or visual evidence, users should enter a
hyperlink directing users to this digital evidence, and include a
timestamp, if appropriate. </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
</FONT><I>Nyāya Sūtra</I></FONT>’s
definition of perception stipulates necessary and sufficient
conditions that sensory stimulations must satisfy to generate
perceptual knowledge. For example, suppose a user wishes to use
perception to support the claim, “Cats and dogs sometimes get
along.”</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
first condition, </FONT><B>sense-object
contact</B></FONT>, requires that
the linked content present sights and/or sounds directly of the
subject of the claim—that is, “Cats and dogs.” If the linked
content features images or sounds that are not of cats and dogs—for
example, if the linked content features dogs but not cats, or cartoon
renderings of cats and dogs—then the intended support would not
satisfy the first criterion of perceptual evidence.   </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
second condition requires that perceptual evidence </FONT><B>does
not depend on language </B></FONT>(</FONT><I>avyapadeśyam</I></FONT>).
Nyāya philosophers variously interpreted this criterion. For the
purposes of this iteration of the Vāda Project platform, the
condition requires that all users, regardless of their linguistic
backgrounds, would assent to the claim (assuming the proposition were
faithfully translated) solely on the basis of the provided sensory
evidence. Hindi, Urdu and English speakers could all presumably agree
that cats and dogs sometimes get along on the basis of a video of a
cat and dog closely resting together.     </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
second condition further helps to ensure that claims that users
intend to support through perception minimize editorializing. The
provided sensory evidence should provoke users’ assent to the claim
regardless of, not only their linguistic backgrounds, but also their
conceptual backgrounds. Reasonable persons can construe “getting
along” in different ways. For example, a video of a cat and dog at
play could fail to satisfy the second condition if, based on the
provided sensory evidence, some users believe the video illustrates
one animal abusing the other.     </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">Finally,
the second condition also helps to distinguish perception from
testimony. Testimony uses words to produce knowledge about a state of
affairs. In contrast, perception uses sense faculties to produce
knowledge of perceptible objects in ways that do not depend on
language. A trustworthy authority might inform us through digital
oral testimony that their cat and dog get along. However, perception
of this oral evidence may only establish that the person has uttered
particular sounds, such as the utterance, “My cat and dog get
along.”  </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
third condition, </FONT><B>is
inerrant</B></FONT>, requires
that the perceptual evidence not be false. In the classical
literature, this criterion is said to exclude perceptual illusions.
In his commentary on </FONT><I>Nyāya
Sūtra </I></FONT>1.1.4,
Vātsyāyana writes:</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-left: 0.5in; margin-bottom: 0.11in">During
the summer, the sun rays and the warmth radiating from the hot ground
pulsate together and come into sensory connection with the visual
organ of a person situated at a distance. In such a situation, the
cognition “Water” arises for the observer owing to the connection
between his sense organ and the object. So to exclude such false
cognition from the definition of perception proper, the author of the
sutras includes the qualifier “</FONT><B>inerrant</B></FONT>.”
(Dasti and Phillips: 23).</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">In
contemporary contexts, this third criterion would exclude “deep
fakes,” in which A/V evidence is manipulated to establish something
that is not the case. It would also exclude perceptual evidence that
is sublated by further perceptual evidence. For example, a video of a
cat and dog resting together that was submitted to support the claim
that “Cats and dogs sometimes get along” would fail to satisfy
this criterion if, later in the video, one of the animals wakes in
apparent terror of its proximity to the other. Similarly, to use a
more pressing example, it would exclude perceptual evidence that has
been taken out of context. For example, in 2018, a video circulated
through WhatsApp that appeared to show a gang of “child lifters”
kidnapping children off streets in India. The video camera captured a
real perceptual event and therefore satisfied the first condition of
perceptual knowledge. Further, because the video was free of
dialogue, it also satisfied the second condition of perceptual
knowledge. However, subsequently released perceptual evidence showed
that the video “was part of a public service announcement” that
sought to warn parents to be vigilant against child abductions.<A CLASS="sdendnoteanc" NAME="sdendnote3anc" HREF="#sdendnote3sym"><SUP>iii</SUP></A>
Just as further perceptual activity sometimes reveals that a
perceived “snake” is a rope, in this case, further perceptual
activity revealed that the perceived “child kidnapping” was
theater. Conversely, unlike false perceptual cognitions, genuine
perceptual knowledge cannot be defeated by later perceptual evidence
that itself meets the conditions of perceptual knowledge.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
fourth and final condition, </FONT><B>is
definitive</B></FONT>, requires
that the perceptual evidence be unambiguous. In the classical
literature, prior to Vācaspatimiśra’s significant
reinterpretation of </FONT><I>Nyāya
Sūtra</I></FONT> 1.1.4 in
response to technical challenges by Buddhist and Mīmāṃsā
philosophers, the fourth criterion was intended to exclude sensory
evidence that provides doubtful or uncertain support for perceptual
judgment. As Vātsyāyana writes in his commentary on the sūtra, “A
person looking at something at a distance is unable to determine
precisely what it is, whether it is smoke or a cloud of dust. So to
exclude from the ranks of genuine perception such unclear cognition
which does arise from a connection between a sense faculty and an
object, the sūtra-maker uses the qualifier “</FONT><B>definitive</B></FONT>”
(Dasti and Phillips: 23).  </FONT>
</P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">To
support a claim through perception, users should select “perception”
as a support means for the claim. They will then be prompted to enter
a URL that links to the audio/video source in a text box labeled
“</FONT><B>Enter link to A/V
evidence</B></FONT>,” and, if
applicable, to enter an appropriate timestamp for the linked A/V
evidence in a text box labeled “Timestamp for audio/video, if
applicable.” </FONT>
</P>
