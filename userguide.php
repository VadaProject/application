the case of a kitchen.</FONT></P>
<P ALIGN=JUSTIFY STYLE="margin-bottom: 0.11in">The
thesis statement of the inference conjoins the subject with the
target property of the claim. The reason statement conjoins the same
subject with the reason property. The rule and example statement is
an automatically generated assertion that the target property is
present in whatever the reason property is present in, and that a
similar subject exemplifies this universal association.</FONT></P>
