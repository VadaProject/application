<h2 id="Perception"> Perception </h2></span></i><span
style='font-family:"Times New Roman",serif'><o:p></o:p></span></p>

Perception
(<i>pratyak&#7779;a</i>) is a way of generating knowledge about something
uncertain through sensory evidence. As <span class=SpellE>Ak&#7779;ap&#257;da</span>
Gautama writes in <i>Ny&#257;ya S&#363;tra</i> 1.1.4, “Perceptual knowledge
arises from connection of a sense faculty and object, does not depend on
language, is inerrant, and is definitive” (<span class=SpellE>Dasti</span> and
Phillips: 20).<o:p></o:p></span></p>

For
the purpose of this digital platform for public debate, perceptual faculties
are limited to the <b>audio and visual senses</b>, as smelling, tasting
touching, and inner understanding (<i>buddhi</i>) lack corresponding objects in
digital space. Conversely, contemporary A/V technologies allow hearing and
seeing to connect with audible and visible objects in the digital realm. To
support claims about smells, tastes, touches and inner awareness, users should instead
rely on testimony as a support means.<span style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

